const assert = require('assert');

/**
 * @param {character[][]} board
 * @param {string} word
 * @return {boolean}
 */
function exist(board, word) {
  if (board.length === 0 || board[0].length === 0 || word.length === 0) { return false; }

  function dfs(i, j, c) {
    // Base case: we've reached the length with valid path.
    if (c === word.length) { return true; }

    // Determine is invalid neighbour or current char not good.
    if (i < 0
          || i >= board.length
          || j < 0
          || j >= board[0].length
          || word[c] !== board[i][j]) {
      return false;
    }

    const temp = board[i][j]; // Current char valid, keep going.
    board[i][j] = '#'; // Track visit

    const res = dfs(i + 1, j, c + 1)
                  || dfs(i - 1, j, c + 1)
                  || dfs(i, j + 1, c + 1)
                  || dfs(i, j - 1, c + 1);
    board[i][j] = temp; // Backtrack visit
    return res;
  }

  const firstLetterIdxs = findLetterIdxs(board, word[0]);
  for (const current of firstLetterIdxs) {
    if (dfs(current.i, current.j, 0)) { return true; }
  }

  return false;
}

/**
 * Less optimal solution due to use of object literals and lack of
 * closures but somewhat more readible thanks to the verboseness.
 * @param {character[][]} board
 * @param {string} word
 * @return {boolean}
 */
function exist2(board, word) {
  const visited = [];
  for (let i = 0; i < board.length; i += 1) {
    visited[i] = [];
    for (let j = 0; j < board[0].length; j += 1) {
      visited[i][j] = false;
    }
  }
  const firstLetterIdxs = findLetterIdxs(board, word[0]);
  for (let i = 0; i < firstLetterIdxs.length; i += 1) {
    const wordFoundFromFirstLetter = existHelper(board, word, visited, firstLetterIdxs[i], '');
    if (wordFoundFromFirstLetter) { return true; }
    setFalseMatrix(visited, board.length, board[0].length);
  }
  return false;
}

function setFalseMatrix(matrix, m, n) {
  for (let i = 0; i < m; i += 1) {
    matrix[i] = [];
    for (let j = 0; j < n; j += 1) {
      matrix[i][j] = false;
    }
  }
}

function existHelper(board, word, visited, current, sofar) {
  visited[current.i][current.j] = true;
  sofar += board[current.i][current.j];

  if (!word.startsWith(sofar)) {
    return false;
  }
  if (word === sofar) {
    return true;
  }
  const neighbours = findNeighbourIdxs(board, current);
  if (neighbours.left !== null && !visited[neighbours.left.i][neighbours.left.j]) {
    if (existHelper(board, word, visited, neighbours.left, sofar)) { return true; }
    visited[neighbours.left.i][neighbours.left.j] = false;
  }
  if (neighbours.up !== null && !visited[neighbours.up.i][neighbours.up.j]) {
    if (existHelper(board, word, visited, neighbours.up, sofar)) { return true; }
    visited[neighbours.up.i][neighbours.up.j] = false;
  }
  if (neighbours.right !== null && !visited[neighbours.right.i][neighbours.right.j]) {
    if (existHelper(board, word, visited, neighbours.right, sofar)) { return true; }
    visited[neighbours.right.i][neighbours.right.j] = false;
  }
  if (neighbours.down !== null && !visited[neighbours.down.i][neighbours.down.j]) {
    if (existHelper(board, word, visited, neighbours.down, sofar)) { return true; }
    visited[neighbours.down.i][neighbours.down.j] = false;
  }

  return false;
}

function findLetterIdxs(board, letter) {
  const idxs = [];
  for (let i = 0; i < board.length; i += 1) {
    for (let j = 0; j < board[0].length; j += 1) {
      if (board[i][j] === letter) {
        idxs.push({
          i,
          j,
        });
      }
    }
  }
  return idxs;
}

function findNeighbourIdxs(board, current) {
  const idxs = {
    left: {},
    up: {},
    right: {},
    down: {},
  };
  const { i, j } = current;
  if (j > 0) { // left
    idxs.left = { i, j: j - 1 };
  } else {
    idxs.left = null;
  }
  if (i > 0) { // up
    idxs.up = { i: i - 1, j };
  } else {
    idxs.up = null;
  }
  if (j < board[0].length - 1) { // right
    idxs.right = { i, j: j + 1 };
  } else {
    idxs.right = null;
  }
  if (i < board.length - 1) { // down
    idxs.down = { i: i + 1, j };
  } else {
    idxs.down = null;
  }
  return idxs;
}

